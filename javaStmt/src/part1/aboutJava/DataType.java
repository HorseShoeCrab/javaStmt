package part1.aboutJava;
/*
{ , } 은 브레이스라고 부르며, {} 합쳐서는 클래스 바디(class body) 라고 부른다.
*/
/*
 현재 파일을 저장할 때는 CTRL + S
 작업중인 모든 파일을 저장할 때는 CTRL + Shift + S
 습관적으로 CTRL + Shift + S
 */
public class DataType {
	/*
	 콘솔창에 보여지는 내용은 나중에 웹개발과정(JEE, JSP 과정) 에서
	 화면(브라우저) 에 보여지는 내용임, 그리고 코딩한 내용을 콘솔에 
	 보요지도록 하는 함수(자바에스는 함수를 "메소드" 라고 칭함) 를
	 main() 메소드라고 한다
	 단축키는 main + CTRL + Space
	 */
	public static void main(String[] args) {
		// 당분간은 별 말이 없으면 무조건 메소드 바디 내부에 코딩함
		int num = 100; // ; 을 구분자라고 한다.
		// syso + CTRL + Space 
		// 콘솔의 결과값을 출력하는 단축키
		System.out.println(num); 
		// 이 액션을 sysout 메소드의 파라미터 값으로 num을 넘겨준다
		
	}

	}
